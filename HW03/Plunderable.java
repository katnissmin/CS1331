/**
 * Creates a new Plunderable, an interface implemented when an object can be plundered.
 */

/**
 * @author Katniss
 * @version 1.0
 */

public interface Plunderable {
    /**
     * Creates a method which takes no parameters but not implemented in Plunderable.
     * @return array of Loot objects
     */
    Loot[] bePlundered();
}
